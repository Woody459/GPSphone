    private void createDocInFirebase(Location mLastKnownLocation) {

        myGeoPoint = new GeoPoint(mLastKnownLocation.getLatitude(), mLastKnownLocation.getLongitude());
        Map<String, Object> geopoint = new HashMap<>();
        geopoint.put((etSend.getText().toString()), myGeoPoint);
        Log.d(TAG, "myGeoPoint!: " + myGeoPoint);
        mDb.collection("User Location").document(etSend.getText().toString()).set(geopoint)
                .addOnSuccessListener(new OnSuccessListener<Void>() {
                    @Override
                    public void onSuccess(Void aVoid) {
                        Log.d(TAG, "DocumentSnapshot successfully written!");
                    }
                })
                .addOnFailureListener(new OnFailureListener() {
                    @Override
                    public void onFailure(@NonNull Exception e) {
                        Log.w(TAG, "Error writing document", e);
                    }
                });
    }


